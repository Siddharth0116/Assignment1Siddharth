class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Output for loop");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        int i = 1;
        System.out.println("Output while loop");
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
